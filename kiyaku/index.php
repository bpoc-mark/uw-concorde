<p><strong>(Scope of Application)</strong></p>
<ul class="none-list-4">
	<li>Article 1. Contracts for Accommodation and related agreements to be entered into between this Hotel and the Guest to be accommodated shall be subject to these Terms and Conditions. And any particulars not provided for herein shall be governed by laws and regulations and/or generally accepted practices.</li>
	<li>2. In the case when the Hotel has entered into a special contract with the Guest insofar as such special contract does not violate laws and regulations and generally accepted practices, notwithstanding the preceding Paragraph, the special contract shall take precedence over the provisions of these Terms and Conditions.</li>
</ul>
<p><strong>(Application for Accommodation Contracts)</strong></p>
<ul class="none-list-4">
	<li>Article 2. A Guest who intends to make an application for an Accommodation Contract with the Hotel shall notify the Hotel of the following particulars:
    <ul class="none-list">
      <li>(1)	Name of the Guest(s);</li>
      <li>(2)	Date of accommodation and estimated time of arrival;</li>
      <li>(3)	Accommodation Charges(based in principle, on the Basic Accommodation Charges listed in the Attached Table No.1); and</li>
      <li>(4)	Other particulars deemed necessary by the Hotel.</li>
    </ul>
  </li>
	<li>2. In the case when the Guest requests, during his stay, extension of the accommodation beyond the date in subparagraph (2) of the preceding Paragraph, it shall be regarded as an application for a new Accommodation Contract at the time such request is made.</li>
</ul>
<p><strong>(Conclusion of Accommodation Contracts, etc)
</strong></p>
<ul class="none-list-4">
	<li>Article 3. A Contract for Accommodation shall be deemed to have been concluded when the Hotel has duly accepted the application as stipulated in the preceding Article. However, the same shall not apply where it has been proved that the Hotel has not accepted the application.</li>
	<li>2. When a Contract for Accommodation has been concluded in accordance with the provisions of the preceding Paragraph, the Guest is requested to pay an accommodation deposit fixed by the Hotel within the limits of Basic Accommodation Charges covering the Guest's entire period of stay (3 days when the period of stay exceeds 3 days) by the date specified by the Hotel.</li>
	<li>3. The deposit shall be first used for the Total Accommodation Charges to be paid by the Guest, then secondly for the cancellation charges under Article 6 and thirdly for the reparations under Article 18 as applicable, and the remainder, if any, shall be refunded at the time of the payment of the Accommodation Charges as stated in Article 12.</li>
	<li>4. When the Guest has failed to pay the deposit by the date as stipulated in Paragraph 2, the Hotel shall treat the Accommodation Contract as invalid. However, the same shall apply only in the case where the Guest is thus informed by the Hotel when the period of payment of the deposit is specified.</li>
</ul>
<p><strong>(Special Contracts Requiring No Accommodation Deposit)</strong></p>
<ul class="none-list-4">
	<li>Article 4. Notwithstanding the provisions of Paragraph 2 of the preceding Article, the Hotel may enter into a special contract requiring no accommodation deposit after the Contract has been concluded as stipulated in the same Paragraph.</li>
	<li>2. In the case when the Hotel has not requested the payment of the deposit as stipulated in Paragraph 2 of the preceding Article and/ or has not specified the date of the payment of the deposit at the time the application for an Accommodation Contract has been accepted, it shall be treated as that the Hotel has accepted a special contract prescribed in the preceding Paragraph.</li>
</ul>
<p><strong>(Refusal of Accommodation Contracts)</strong></p>
<ul class="none-list-4">
	<li>Article 5. The Hotel may not accept the conclusion of an Accommodation Contract in any of the following cases:
    <ul class="none-list">
      <li>(1) When the application for accommodation does not conform to these Terms and Conditions;</li>
      <li>(2) When the Hotel is fully booked and no rooms are available;</li>
      <li>(3) In the event that a natural disaster, structural damage or any other unforeseen circumstances make it impossible to provide accommodation;</li>
      <li>(4) When the Hotel receives an unreasonable demand with regard to accommodation;</li>
      <li>(5) When the applicant is considered liable to conduct himself in a manner that contravenes the provisions of the law or disrupts public order;</li>
      <li>(6) When the applicant tries to threaten or coerce the Hotel, or any of the staff, with violence, blackmail or in any other way, or has done so in the past;</li>
      <li> (7) When the applicant is causing a major disturbance, danger, a sense of fear or annoyance to other guests;</li>
      <li> (8) When the applicant is a member of an organized criminal syndicate actively participating in organized crime;<br>(In accordance with the organized crime prevention Laws that went into effect March 1st 1992.)</li>
      <li>(9)	When the applicant is known to be a high ranking official of organized criminal group or is involved in the dealings of an organized criminal group;</li>
      <li>(10) When the applicant is clearly carrying an infectious disease;</li>
      <li>(11) When the applicant has severe mental problems and / or can not control himself due to the effects of medication or alcohol or is a risk to the safety of both himself and others;</li>
      <li>(12) When the applicant is excessively drunk or his words and actions cause annoyance to other guests;<br>(In accordance with the provisions of Article 5 of the Shizuoka ken ordinance concerning the enforcement of Ryokan business laws.)</li>
      <li>(13) When the applicant is unhygienic and unkempt or his attire causes annoyance to other guests;</li>
      <li>(14) When the applicant is acting suspiciously or there is a justifiable reason to refuse accommodation;</li>
      <li>(15) When it is clear that the applicant can not pay;</li>
      <li>(16) When the applicant breaks the 2nd clause of Article 6 of a Tourism law and refuses to give his name;</li>
