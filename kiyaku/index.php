</ul>
<p><strong>(Registration)</strong></p>
<ul class="none-list-4">
	<li>Article 8. The Guest shall register the following particulars at the front desk of the Hotel on the day of accommodation;
    <ul class="none-list">
      <li>(1) Name, age, sex, address and occupation of the Guest (s);</li>
      <li>(2) Except Japanese, nationality, passport number, port and date of entry in Japan;</li>
      <li>(3) Date and estimated time of departure; and </li>
      <li>(4) Other particulars deemed necessary by the Hotel.</li>
    </ul>
  </li>
	<li>2. In the case when the Guest intends to pay his Accommodation Charges prescribed in Article 12 by any means other than Japanese currency, such as traveler's cheques, coupons or credit cards, these credentials shall be shown in advance at the time of the registrations prescribed in the preceding Paragraph.</li>
</ul>
<p><strong>(Occupancy Hours of Guest Rooms)</strong></p>
<ul class="none-list-4">
