	<li>2. Accommodation Charges, etc. as stated in the preceding Paragraph shall be paid Japanese currency or by any means other than Japanese currency such as traveler's cheques, coupons or credit cards recognized by the Hotel at the front desk at the time of the departure of the Guest or upon request by the Hotel.</li>
