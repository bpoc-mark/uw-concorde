	<li>3. In the case when the Guest does not appear by 8 p.m. of the accommodation date (2 hours after the expected time of arrival if the Hotel is notified of it) without an advance notice, the Hotel may regard the Accommodation Contract as being cancelled by the Guest.</li>
</ul>
<p><strong>(Right to Cancel Accommodation Contracts by the Hotel)</strong></p>
<ul class="none-list-4">
	<li>Article 7. The Hotel may cancel the Accommodation Contract in any of the following cases:
    <ul class="none-list">
      <li>(1) If at any time whether prior to or during a guest's stay a situation listed in the provisions of Article 5 sections 3 to 17 becomes apparent;</li>
      <li>(2) When a guest breaks the 2nd clause of Article 6 of a Tourism law and does not give an address and name or when a false address and name is given;</li>
      <li>(3) When the guest engages in prohibited actions such as smoking in bed or the unauthorized use of fire fighting equipment (Only to be used in the prevention of a fire.);</li>
      <li>(4) Any other actions not in accordance with hotel rules and regulations;</li>
    </ul>
  </li>
